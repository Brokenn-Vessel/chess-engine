int evaluate(Board* board) {
    int score = 0 ;
    u64 bitboard ;

    for(int piece = P; piece <= k; piece++) {
        bitboard = board->bitboards[piece] ;

        while(bitboard) {
            int square = get_ls1b_index(bitboard) ;
            score += materialScore[piece] ;
            switch(piece) {
                case P : 
                    score += pawnScore[square] ;
                    break ;
                case N : 
                    score += knightScore[square] ;
                    break ;
                case B : 
                    score += bishopScore[square] ;
                    break ;
                case R : 
                    score += rookScore[square] ;
                    break ;
                case K : 
                    score += kingScore[square] ;
                    break ;

                case p : 
                    score -= pawnScore[mirror[square]] ;
                    break ;
                case n : 
                    score -= knightScore[mirror[square]] ;
                    break ;
                case b : 
                    score -= bishopScore[mirror[square]] ;
                    break ;
                case r : 
                    score -= rookScore[mirror[square]] ;
                    break ;
                case k : 
                    score -= kingScore[mirror[square]] ;
                    break ;

            }
            pop_ls1b(bitboard) ;
        }
    }
    return (board->side == WHITE) ? score : -score;
}